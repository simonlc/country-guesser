export function weightedShuffle(array) {
  const arr = [...array];
  const newWeights = arr.map(([key, value]) => [
    key,
    Math.pow(Math.random(), 1 / value),
  ]);
  newWeights.sort((a, b) => {
    return a[1] < b[1] ? 1 : -1;
  });
  return newWeights;
}
